    public ReportTotalStaffPosition() {
        initComponents();
        setVisible(true);
        setLocationRelativeTo(null);
        int managerCount = 0;
        int normalStaffCount = 0;
        int total = 0;
        MaintainStaffControl sControl = new MaintainStaffControl();
        ResultSet rs = sControl.retrieveRecord();

        try {
            while (rs.next()) {
                if (rs.getString(7).equals("Manager")) {
                    managerCount++;
                    total++;
                } else {
                    normalStaffCount++;
                    total++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReportTotalStaffPosition.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTable1.setValueAt("Manager", 0, 0);
        jTable1.setValueAt(managerCount, 0, 1);
        jTable1.setValueAt("Normal Staff", 1, 0);
        jTable1.setValueAt(normalStaffCount, 1, 1);
        
        jTextField1.setText(""+total);
    }

