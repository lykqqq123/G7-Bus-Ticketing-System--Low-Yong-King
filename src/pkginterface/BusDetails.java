    private void DescriptionTACaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_DescriptionTACaretUpdate
        if (!BusIDTF.getText().equals("") && !BusPlateNumTF.getText().equals("") && !BusColourTF.getText().equals("") && !TotalSeatTF.getText().equals("") && !DescriptionTA.getText().equals("")) {
            UpdateBTN.setEnabled(true);
        } else if (BusIDTF.getText().equals("") || BusPlateNumTF.getText().equals("") || BusColourTF.getText().equals("") || TotalSeatTF.getText().equals("") || DescriptionTA.getText().equals("")) {
            UpdateBTN.setEnabled(false);
        }
    }//GEN-LAST:event_DescriptionTACaretUpdate

