    private void UpdateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBTNActionPerformed
        String busPlateNumRE1 = "[A-Z]{1,3}+ +[0-9]{1,4}+ +[A-Z]{0,1}";
        String busPlateNumRE2 = "[A-Z]{1,3}+ +[0-9]{1,4}";
        String busColourRE = "[a-zA-Z]{3,7}";

        try {
            if (BusPlateNumTF.getText().matches(busPlateNumRE1) || BusPlateNumTF.getText().matches(busPlateNumRE2)) {
                if (BusColourTF.getText().matches(busColourRE) && Integer.parseInt(TotalSeatTF.getText()) < 31 && !DescriptionTA.getText().equals("")) {
                    BusDetailsDomain db = new BusDetailsDomain(BusIDTF.getText(), BusPlateNumTF.getText(), BusColourTF.getText(), (String) BusStatusCB.getSelectedItem(), Integer.parseInt(TotalSeatTF.getText()), DescriptionTA.getText(), (String) BusTypeCB.getSelectedItem());
                    bdControl.updateRecord(db);
                    JOptionPane.showMessageDialog(null, "The Bus Details is updated!");
                    BusIDTF.setText("");
                    BusPlateNumTF.setText("");
                    BusColourTF.setText("");
                    TotalSeatTF.setText("");
                    BusStatusCB.setSelectedIndex(0);
                    DescriptionTA.setText("");
                    BusTypeCB.setSelectedIndex(0);
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid Input\nBus Plate Number : ABC 1234 / AB 1234 C.\n"
                            + "BusColour      : Only Character.\n"
                            + "TotalSeat      : >0;<= 30.\n"
                            + "Description    : Cannot be blank."
                            + "*UpperCase Character for Bus Plate Number.*", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Input\nBus Plate Number : ABC 1234 / AB 1234 C.\n"
                        + "BusColour      : Only Character.\n"
                        + "TotalSeat      : >0;<= 30.\n"
                        + "Description    : Cannot be blank."
                        + "*UpperCase Character for Bus Plate Number.*", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Input\nBus Plate Number : ABC 1234 / AB 1234 C.\n"
                    + "BusColour      : Only Character.\n"
                    + "TotalSeat      : >0;<= 30.\n"
                    + "Description    : Cannot be blank."
                    + "*UpperCase Character for Bus Plate Number.*", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_UpdateBTNActionPerformed

