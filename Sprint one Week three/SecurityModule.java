    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String ID = StaffID1.getText();
        Staff staff = staffControl.selectRecord(ID);
        if (ID.equals("")) {
            JOptionPane.showMessageDialog(null, "You Must Key In An Existing Staff ID", "ERROR!", JOptionPane.ERROR_MESSAGE);
        } else {
            if (staff != null) {
                if (ID.equals(staff.getStaffId()) && Pwd2.getText().equals(staff.getStaffPwd())) {
                    if (Pwd3.getText().equals(jtfConfirmPwd.getText())) {
                        Staff newPwd = new Staff(StaffID1.getText(), Pwd3.getText());
                        staffControl.setPwd(newPwd);
                        JOptionPane.showMessageDialog(null, "Password Changed Successful.");
                        StaffID1.setText("");
                        Pwd2.setText("");
                        Pwd3.setText("");
                        jtfConfirmPwd.setText("");
                        StaffID.requestFocusInWindow();
                    } else {
                        JOptionPane.showMessageDialog(null, "You have inserted a different PASSWORD!");
                        StaffID1.setText("");
                        Pwd2.setText("");
                        Pwd3.setText("");
                        jtfConfirmPwd.setText("");
                        StaffID1.requestFocusInWindow();
                    }

                } else if (attempt > 2) {
                    JOptionPane.showMessageDialog(null, "You have exceeded the limit attempt.", "ERROR!", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                } else {

                    JOptionPane.showMessageDialog(null, "Invalid username/password! Please try again!");
                    attempt++;
                    StaffID1.setText("");
                    Pwd2.setText("");
                    Pwd3.setText("");
                    jtfConfirmPwd.setText("");
                    StaffID1.requestFocusInWindow();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Staff ID!", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

