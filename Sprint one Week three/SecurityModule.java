    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        String ID = StaffID.getText();
        Staff staff = staffControl.selectRecord(ID);
        if (ID.equals("")) {
            JOptionPane.showMessageDialog(null, "You Must Key In An Existing Staff ID", "ERROR!", JOptionPane.ERROR_MESSAGE);
        } else {
            if (staff != null) {
                if (ID.equals(staff.getStaffId()) && Pwd1.getText().equals(staff.getStaffPwd())) {
                    String position = staff.getStaffPosition();
                    try{
                         new StaffMaintenance().setLoginUser(ID);
                    }catch (Exception ex) {
                            JOptionPane.showMessageDialog(null, "ERROR!");
                        }
                    if (position.equals("Manager")) {
                        JOptionPane.showMessageDialog(null, "Login successfull as Manger. Welcome to Seri Meju Bus Ticketing System.");
                        setVisible(false);
                        defineUser = "M";
                        new MainMenu().setDefineUser(defineUser);
                        
                    } else if (position.equals("Normal Staff")) {
                        JOptionPane.showMessageDialog(null, "Login successfull as Employee. Welcome to Seri Meju Bus Ticketing System.");
                        setVisible(false);
                        defineUser = "E";
                        new MainMenu().setDefineUser(defineUser);
                    }
                    
                } else if (attempt > 2) {
                    JOptionPane.showMessageDialog(null, "You have exceeded the limit attempt.", "ERROR!", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                } else {

                    JOptionPane.showMessageDialog(null, "Invalid username/password! Please try again!");
                    attempt++;
                    StaffID.setText("");
                    Pwd1.setText("");
                    StaffID.requestFocusInWindow();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Staff ID!", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_LoginActionPerformed

